valor1 = 20
valor2 = 40

print('Quando comparo {} com {} usando o operador ==, tenho {}'.format(valor1, valor2, valor1 == valor2))


print('Quando comparo {} com {} usando o operador ==, tenho {}'.format(valor1, valor2, valor1 > valor2))

print('Quando comparo {} com {} usando o operador ==, tenho {}'.format(valor1, valor2, valor1 < valor2))

print('Quando comparo {} com {} usando o operador ==, tenho {}'.format(valor1, valor2, valor1 >= valor2))

print('Quando comparo {} com {} usando o operador ==, tenho {}'.format(valor1, valor2, valor1 <= valor2))

print('Quando comparo {} com {} usando o operador ==, tenho {}'.format(valor1, valor2, valor1 != valor2))